package dto;

public class PurpleRestriction {
    private Enumeration pattern;
    private String base;
    private Prefix prefix;

    public Enumeration getPattern() {
        return pattern;
    }

    public void setPattern(Enumeration value) {
        this.pattern = value;
    }

    public String getBase() {
        return base;
    }

    public void setBase(String value) {
        this.base = value;
    }

    public Prefix getPrefix() {
        return prefix;
    }

    public void setPrefix(Prefix value) {
        this.prefix = value;
    }
}
