package dto;

public class FluffyComplexType {
    private TentacledSequence sequence;
    private Prefix prefix;
    private PurpleSimpleContent simpleContent;

    public TentacledSequence getSequence() {
        return sequence;
    }

    public void setSequence(TentacledSequence value) {
        this.sequence = value;
    }

    public Prefix getPrefix() {
        return prefix;
    }

    public void setPrefix(Prefix value) {
        this.prefix = value;
    }

    public PurpleSimpleContent getSimpleContent() {
        return simpleContent;
    }

    public void setSimpleContent(PurpleSimpleContent value) {
        this.simpleContent = value;
    }
}
