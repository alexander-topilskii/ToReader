package dto;

public class FluffyAttribute {
    private Ref ref;
    private Prefix prefix;

    public Ref getRef() {
        return ref;
    }

    public void setRef(Ref value) {
        this.ref = value;
    }

    public Prefix getPrefix() {
        return prefix;
    }

    public void setPrefix(Prefix value) {
        this.prefix = value;
    }
}
