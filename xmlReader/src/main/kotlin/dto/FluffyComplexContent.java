package dto;

public class FluffyComplexContent {
    private StickyExtension extension;
    private Prefix prefix;
    private String mixed;

    public StickyExtension getExtension() {
        return extension;
    }

    public void setExtension(StickyExtension value) {
        this.extension = value;
    }

    public Prefix getPrefix() {
        return prefix;
    }

    public void setPrefix(Prefix value) {
        this.prefix = value;
    }

    public String getMixed() {
        return mixed;
    }

    public void setMixed(String value) {
        this.mixed = value;
    }
}
